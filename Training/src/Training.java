//Training class-Base class
public class Training {

	private int id;
	protected String subject;
	private int fees;

	public Training(int id, String subject, int fees) {
		super();
		this.id = id;
		this.subject = subject;
		this.fees = fees;

	}

	public int getFees() {
		return fees;
	}

	 public int getOrderValue() {
		return fees;
	 }
	 

	
		 
	 }


